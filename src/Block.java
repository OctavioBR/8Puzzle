public class Block {
	private int value;
	private int x;
	private int y;

	public int getValue() {
		return value;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public void setValue(int value) {
		this.value = value;
	}
}
